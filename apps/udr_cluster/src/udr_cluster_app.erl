-module(udr_cluster_app).
-moduledoc "Application callback for `udr_cluster`; joins this node to the session scope.".
-behaviour(application).

-export([start/2, stop/1]).

-spec start(application:start_type(), term()) -> {ok, pid()} | {error, term()}.
start(_StartType, _StartArgs) ->
    ok = syn:add_node_to_scopes([udr_cluster:scope()]),
    udr_cluster_sup:start_link().

-spec stop(term()) -> ok.
stop(_State) ->
    ok.
